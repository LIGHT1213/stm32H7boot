void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	static uint8_t count;
	if(huart==&huart2)
	{
		if(count==0&&RecBuff[count]!=0x01)
		{
			count=0;
		}
		else if(count==1&&RecBuff[count]!=0xff)
		{
			count=0;
		}
		else if(count==2&&RecBuff[count]!=0xff)
		{
			count=0;
		}
		else
		{
			if(count<=22)
			{
				count++;
			}
			else
			{
				count=0;
				RecBuffFlag=1;
			}
		}
		HAL_UART_Receive_IT(&huart2,&RecBuff[count],1);
	}
}
